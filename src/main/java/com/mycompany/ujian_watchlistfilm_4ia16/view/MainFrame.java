    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
    try {
        Media m = new Media();
        m.setJudul(txtJudul.getText());
        m.setJenis(cbJenis.getSelectedItem().toString());
        m.setTahunRilis(Integer.parseInt(txtTahun.getText()));
        m.setGenre(txtGenre.getText());
        m.setSinopsis(txtSinopsis.getText());
        m.setStatusTontonan("Watchlist");
        m.setRatingPribadi(0.0f);

        mediaRepository.save(m);
        loadData();
        clearForm();
        JOptionPane.showMessageDialog(this, "Berhasil Disimpan!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal! Cek Input Tahun.");
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

