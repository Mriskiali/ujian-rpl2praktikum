    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:                                       
        // Ambil text dari kolom pencarian
        String keyword = txtSearch.getText();
        // Panggil Repository
        List<Media> result = mediaRepository.findByJudulContainingIgnoreCase(keyword);
        
        // Bersihkan tabel lama
        tableModel.setRowCount(0);
        
        // Isi tabel dengan hasil pencarian
        for (Media m : result) {
            tableModel.addRow(new Object[]{
                m.getId(), m.getJudul(), m.getJenis(), m.getTahunRilis(), 
                m.getGenre(), m.getStatusTontonan(), m.getRatingPribadi(), m.getSinopsis()
            });
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
    if (selectedId != null) {
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus?");
        if (confirm == JOptionPane.YES_OPTION) {
            mediaRepository.deleteById(selectedId);
            loadData();
            clearForm();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
    if (selectedId != null) {
        Media m = mediaRepository.findById(selectedId).orElse(null);
        if (m != null) {
            String rating = JOptionPane.showInputDialog("Masukkan Rating (0-10):");
            if (rating != null) {
                m.setStatusTontonan("Completed");
                m.setRatingPribadi(Float.parseFloat(rating));
                mediaRepository.save(m);
                loadData();
                clearForm();
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
    }
    }//GEN-LAST:event_btnSelesaiActionPerformed

    private void tblMediaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMediaMouseClicked
        // TODO add your handling code here:
    int row = tblMedia.getSelectedRow();
    if (row != -1) {
        selectedId = Long.parseLong(tblMedia.getValueAt(row, 0).toString());
        txtJudul.setText(tblMedia.getValueAt(row, 1).toString());
        cbJenis.setSelectedItem(tblMedia.getValueAt(row, 2).toString());
        txtTahun.setText(tblMedia.getValueAt(row, 3).toString());
        txtGenre.setText(tblMedia.getValueAt(row, 4).toString());
        // Sinopsis ada di kolom index 7 (sesuaikan urutan kolom tabel kamu)
        txtSinopsis.setText(tblMedia.getValueAt(row, 7).toString());
    }
    }//GEN-LAST:event_tblMediaMouseClicked

